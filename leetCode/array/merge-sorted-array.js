var merge = function (nums1, m, nums2, n) {
  var i = 0,
    j = 0;
  while (i < m && j < n) {
    if (nums1[i] <= nums2[j]) {
      i++;
    } else {

      var temp = nums1[i];
      nums1[i] = nums2[j];
      nums2[j] = temp;
      nums2.sort((a, b)=> a-b);
    }
  }
  while(j< n){
    nums1[i+j] = nums2[j];
    j++;
  }
  return nums1;
};

