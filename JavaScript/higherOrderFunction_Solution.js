const radius = [3, 1, 2, 4];

const area = function (radius) {
  return Math.PI * radius * radius;
};

const circumference = function (radius) {
  return 2 * Math.PI * radius;
};

const diameter = function (radius) {
  return 2 * radius;
};

//this calculateFormula is Higher Order Function, it has function as a argument
const calculateFormula = function (radius, logic) {
  const output = [];
  for (let i = 0; i < radius.length; i++) {
    output.push(logic(radius[i]));
  }
  return output;
};

console.log(calculateFormula(radius, area));

console.log(calculateFormula(radius, circumference));

console.log(calculateFormula(radius, diameter));
